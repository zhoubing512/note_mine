# 1.判断一个输入的数字是否是质数。

'''
质数的条件：
1、能且仅能被1和自身整除的整数。
2、1不是质数。
3、2是质数。

思路：①：去除特殊的选项
      ②：对剩余的循环取余，判断其中取余的结果有没有为0 的。
'''
num1 = int(input("请您输入一个整数："))

# 首先先写出两种特殊情况。if-elif-else语句。
if num1 == 1:
    print("整数1不是质数。")
elif num1 == 2:
    print("整数2是质数。")

# 针对剩余的用num1来对一个循环递增的量取余。
else:
    for i in range(2 , num1):

# 对取余的结果进行判断，如果对某个值取余的值为0，说明输入的这个整数不是质数。
        if num1 % i == 0:
            print("您输入的整数不是质数。")
            break

# 当增量超过约束范围而没有执行到if下的语句块的时候，说明num1在此范围内没有约数，所以就是质数。
    else:
        print("您输入的整数是质数。")

# 2、打印出所有三位数中的水仙花数。

'''
水仙花数：如果一个三位数字的百位的三次方加上十位的三次方再加上个位的三次方等于这个数字本身，那么
这个数字就是水仙花数。

思路：①：输入一个三位整数，拿出他的各位数字。
      ②：判断这三个数字的三次方相加是不是等于这个数字本身。
'''
s = 0
for num2 in range(100 , 1000):
    a = num2 // 100
    b = num2 % 10
    c = num2 % 100 // 10
    if (a ** 3 + b ** 3 + c ** 3) == num2:
        print(num2)
        s += 1
print("这样的三位水仙花数一共有%d个。" %(s))

# 3、打印九九乘法表

'''
九九乘法表：
1 * 1 = 1
2 * 1 = 2   2 * 2 = 4
…………………………………………

思路：循环嵌套
      ①：定义两个嵌套的循环递增的变量,需要注意的是内部的变量的取值。
      ②：循环相乘，输出结果。
'''
m = 1
while m <10:
    for n in range(1 , m + 1):
        q = m * n
        print("%d * %d = %d" %(n , m ,q) ,end ="\t")
    print("\n")
    m += 1

