# 5、输入一个字符串，将字符串中的大写字母转小写，小写字母转大写。

'''
大小写的互相转化可以利用已有的函数来写。
不用已有函数可以使用字符串拼接的方法来完成。
'''
#使用已有函数。
str1 =input("请输入需要转换的英文字符串：")
str2 = str1.swapcase()
print(str2)

#使用字符串拼接。
str3 = input("请输入需要转换的英文字符串：")
list1 = range(65 , 91)
list2 = range(97 ,123)
for x in range(0 , len(str3)):
    if ord(str3[x]) in list1:
        h = list1.index(ord(str3[x]))
#这是对str3这个变量重新赋值，而不是更改了字符串本身。----字符串不可变是指字符串本身不能更改。
        str3 = str3[: x] + chr(list2[h]) + str3[x + 1 :]
    else:
        z = list2.index(ord(str3[x]))
        str3 = str3[: x] + chr(list1[z]) + str3[x + 1 :]
print(str3)